package Primitives;

public class UnaryPractice {

    public static void main(String[] args) {

        //DD buy 12 donuts get $1 off next time
        //KK buy 12 donuts and pay $11 now

        int dd = 12;
        int kk = 12;

        System.out.println("payment for DD "+ dd--); //shows 12

        System.out.println(" payment for KK "+ --kk ); // shows 11

        System.out.println("DD i am back " + dd); // shows 11

        System.out.println("KK i am back " + kk); // shows 11

       // kk -= 5;










    }
}
