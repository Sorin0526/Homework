package Primitives;

public class CharPractice {

    public static void main(String[] args) {

        char firstletter= 'A'; // value for a char data type needs to be in the single quotation mark ('A')

        System.out.println(firstletter);

        char secondLetter = '6';
        System.out.println(secondLetter);

        char thirdLetter = '^';
        System.out.println(thirdLetter);

        char fourthLetter = 66;
        System.out.println(fourthLetter);

       // char fourthLetter1 = '66''; you can store only single character in single quotation mark for char

        char fifthLetter = 255;
        System.out.println(fifthLetter);

        char x=42;// if u assign a number for a char data type
                  // it will print a  symbol/character from ASCII table
        System.out.println(x);

        double d= '?';// if we assign a symbol/character from ASCII table
                    // it will print the number/value for it

        System.out.println(d);
        // create a int data type and assign a character for it and print it out
        int number= '?';
        System.out.println(number);

        int c = 'C', k = 'K', g = 'G', t= 'T', e = 'E', m = 'M';
        System.out.println("" +c +"---" +k  +"---"+g  +"---"+t +"---" +e  +"---"+m);
        System.out.println(c + k + g + t + e + m);











    }
}
